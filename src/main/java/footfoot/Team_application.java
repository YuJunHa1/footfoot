package footfoot;

public class Team_application {
	private int team_application_id;
	private int team_id;
	private String user_id;
	private int status;
	private String content;
	
	public int getTeam_application_id() {
		return team_application_id;
	}
	public void setTeam_application_id(int team_application_id) {
		this.team_application_id = team_application_id;
	}
	public int getTeam_id() {
		return team_id;
	}
	public void setTeam_id(int team_id) {
		this.team_id = team_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}
